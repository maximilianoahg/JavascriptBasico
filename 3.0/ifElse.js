// If Else + if else

let nota = 5;

if (nota === 5){
    console.log("Felicidades, Has obtenido un sobresaliente");
} else if (nota === 4) {
    console.log("BUEN TRABAJO, PERO PODRIAS HABERLO HECHO MEJOR");
} else if (nota === 3){
    console.log("Obtuviste suficiente");
} else if (nota === 2){
    console.log("Casi no apruebas");
} else if (nota === 1){
    console.log("No aprobaste, estudia un poco mas");
} else {
    console.log("Error, introduce una nota entre 1 y el 5");
}