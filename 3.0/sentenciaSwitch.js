// Sentencias Switch

let nota = 4;

switch(nota){
    case 5:
        console.log("Buen trabajo");
        break;
    case 4:
        console.log("BUEN TRABAJO, PERO PODRIAS HABERLO HECHO MEJOR");
        break;
    case 3:
        console.log("Obtuviste suficiente");
        break;
    default:
        console.log("ERROR");
        break;
}